async function apiDeleteUser(userId){
  const r = await apiFetch('/api/users/' + encodeURIComponent(userId), { method:'DELETE' });
  const json = await r.json();
  if(!r.ok) throw new Error(json.error || 'Failed to delete user');
  return json;
}

function queueSave(project){
  if(!isAdmin()) return;
  const el = document.getElementById('saveStatus');
  if(el) el.textContent = 'Saving...';
  clearTimeout(saveTimer);
  saveTimer = setTimeout(async () => {
    try{
      await apiUpdate(project._id, project);
      if(el) el.textContent = 'Saved to Atlas';
    }catch(e){
      if(el) el.textContent = 'Save failed';
      console.error(e);
    }
  }, 500);
}

