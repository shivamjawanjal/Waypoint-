function computeLayout(project){
  const nodes = project.nodes;
  const byId = {}; nodes.forEach(n => byId[n.id]=n);
  const children = {}; nodes.forEach(n => { children[n.parentId] = children[n.parentId]||[]; if(n.parentId) children[n.parentId].push(n); });
  const roots = nodes.filter(n => !n.parentId || !byId[n.parentId]);
  const pos = {};
  let cursor = 0;

  function visibleChildren(n){
    if(n.collapsed) return [];
    return (children[n.id]||[]).filter(k => isNodeVisible(k));
  }

  function assign(n, depth){
    if(!isNodeVisible(n)){ return; }
    const kids = visibleChildren(n);
    if(kids.length===0){
      pos[n.id] = { x: depth*COL_W, y: cursor*ROW_H, depth };
      cursor += 1;
    } else {
      const startCursor = cursor;
      kids.forEach(k => assign(k, depth+1));
      if(!pos[kids[0]?.id]) return;
      const first = pos[kids[0].id].y;
      const last = pos[kids[kids.length-1].id].y;
      pos[n.id] = { x: depth*COL_W, y: (first+last)/2, depth };
    }
  }
  roots.forEach(r => assign(r, 0));
  return { pos, children, roots };
}

