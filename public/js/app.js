function breadcrumb(project, node){
  const byId = {}; project.nodes.forEach(n=>byId[n.id]=n);
  const chain = [];
  let cur = node;
  while(cur){ chain.unshift(cur.label); cur = cur.parentId ? byId[cur.parentId] : null; }
  return chain.join(' / ');
}

