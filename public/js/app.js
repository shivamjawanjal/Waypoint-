function loadAuth(){
  try{
    const raw = localStorage.getItem(AUTH_KEY);
    if(raw){
      const data = JSON.parse(raw);
      authToken = data.token;
      authUser = data.user;
    }
  }catch(e){}
}
function saveAuth(){
  localStorage.setItem(AUTH_KEY, JSON.stringify({ token: authToken, user: authUser }));
}
function clearAuth(){
  authToken = null;
  authUser = null;
  localStorage.removeItem(AUTH_KEY);
}
function isAdmin(){ return authUser && authUser.role === 'admin'; }
function authHeaders(){ return authToken ? { 'Authorization': 'Bearer ' + authToken, 'Content-Type': 'application/json' } : { 'Content-Type': 'application/json' }; }

