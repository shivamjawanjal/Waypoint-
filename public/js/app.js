function progressOf(project){
  const leafNodes = project.nodes.filter(n => !project.nodes.some(m => m.parentId===n.id));
  if(leafNodes.length===0) return 0;
  const done = leafNodes.filter(n=>n.status==='done').length;
  return Math.round(100*done/leafNodes.length);
}

