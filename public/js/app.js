function buildProjectFromGemini(name, planText, gem){
  const nodes = gem.nodes.map(n => ({
    id: n.id || uid('n'),
    parentId: n.parentId || null,
    type: n.type || 'task',
    label: n.label || '(untitled)',
    status: n.doneHint ? 'done' : 'pending',
    checklist: (n.checklist||[]).map(c => ({ id: uid('c'), text: c, done: false })),
    notes: '',
    collapsed: false,
    assignedTo: null,
    completedBy: null
  }));
  return {
    name: name || gem.title || 'Untitled plan',
    title: gem.title || name || 'Untitled plan',
    provider: 'gemini',
    rawText: planText,
    nodes
  };
}

