const SYSTEM_PROMPT = `You are a project-planning assistant. The user will paste a software implementation plan generated by an AI (often phases, milestones, file lists, and a described workflow like "run linter, run tests, review, merge").

Convert it into JSON with EXACTLY this shape:
{
  "title": "short plan title",
  "nodes": [
    {
      "id": "short-unique-slug",
      "parentId": "id-of-parent-or-null-for-top-level",
      "type": "phase" | "milestone" | "file" | "task",
      "label": "short display label (file name, milestone name, or task name)",
      "doneHint": true or false,
      "checklist": ["concrete small sub-step", "..."]
    }
  ]
}

Rules:
- Preserve the hierarchy implied by the source (Phase contains Milestones; a Milestone contains Files/Tasks). Root nodes (phases) have parentId: null.
- doneHint is true ONLY if the source text explicitly marks that item as already complete (checkmark, "done", "completed").
- If the plan describes a recurring quality-gate workflow anywhere (lint, type-check, tests, code review, merge, etc.), attach a version of that same checklist to every milestone/file/task node that represents implementation work, worded specifically to that node — IN ADDITION to any concrete implementation sub-steps you can infer are needed for that specific file/task (e.g. "handle empty/null input", "write docstrings", "add unit tests for edge case X").
- Checklist items must be small, concrete, and specific — the kind of detail that gets silently skipped when a developer is moving fast between milestones. Avoid vague items like "implement functionality".
- Every node except top-level phases must have a valid parentId matching another node's id.
- Output ONLY the raw JSON object. No markdown code fences, no commentary, no trailing text.`;

async function callGemini(planText, apiKey){
  const url = 'https://generativelanguage.googleapis.com/v1beta/models/gemini-flash-latest:generateContent?key=' + encodeURIComponent(apiKey);
  const body = {
    contents: [{ role:'user', parts:[{ text: SYSTEM_PROMPT + '\n\n--- PLAN TEXT START ---\n' + planText + '\n--- PLAN TEXT END ---' }] }],
    generationConfig: { temperature: 0.4, responseMimeType: 'application/json' }
  };
  const resp = await fetch(url, { method:'POST', headers:{'Content-Type':'application/json'}, body: JSON.stringify(body) });
  if(!resp.ok){
    const t = await resp.text().catch(()=> '');
    throw new Error('Gemini API error ' + resp.status + ': ' + t.slice(0,400));
  }
  const data = await resp.json();
  const parts = data?.candidates?.[0]?.content?.parts || [];
  const text = parts.map(p=>p.text||'').join('');
  if(!text) throw new Error('Empty response from Gemini. Check the API key and try again.');
  let cleaned = text.trim().replace(/^```json/i,'').replace(/^```/,'').replace(/```$/,'').trim();
  let parsed;
  try{ parsed = JSON.parse(cleaned); }
  catch(e){ throw new Error('Could not parse JSON from Gemini response. Raw output:\n' + cleaned.slice(0,600)); }
  if(!parsed.nodes || !Array.isArray(parsed.nodes)) throw new Error('Gemini response was missing a "nodes" array.');
  return parsed;
}

async function callGroq(planText, apiKey){
  const url = 'https://api.groq.com/openai/v1/chat/completions';
  const body = {
    model: 'openai/gpt-oss-120b',
    messages: [
      { role:'system', content: SYSTEM_PROMPT },
      { role:'user', content: '--- PLAN TEXT START ---\n' + planText + '\n--- PLAN TEXT END ---' }
    ],
    temperature: 0.4,
    response_format: { type:'json_object' }
  };
  const resp = await fetch(url, {
    method:'POST',
    headers:{ 'Content-Type':'application/json', 'Authorization':'Bearer '+apiKey },
    body: JSON.stringify(body)
  });
  if(!resp.ok){
    const t = await resp.text().catch(()=>'');
    throw new Error('Groq API error ' + resp.status + ': ' + t.slice(0,400));
  }
  const data = await resp.json();
  const text = data?.choices?.[0]?.message?.content || '';
  if(!text) throw new Error('Empty response from Groq. Check the API key and try again.');
  let cleaned = text.trim().replace(/^```json/i,'').replace(/^```/,'').replace(/```$/,'').trim();
  let parsed;
  try{ parsed = JSON.parse(cleaned); }
  catch(e){ throw new Error('Could not parse JSON from Groq response. Raw output:\n' + cleaned.slice(0,600)); }
  if(!parsed.nodes || !Array.isArray(parsed.nodes)) throw new Error('Groq response was missing a "nodes" array.');
  return parsed;
}

